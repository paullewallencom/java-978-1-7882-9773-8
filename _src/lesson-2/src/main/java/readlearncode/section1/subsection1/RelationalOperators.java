public class RelationalOperators {

    public static void main(String... args) {

        short x = 10;
        byte y = 5;
        float z = 5.5f;

        System.out.println(x > y); // x MORE THAN Y
        System.out.println(z >= y); // z MORE THAN OR EQUAL TO y
        System.out.println(x < y); // X LESS THAN y
        System.out.println(x <= y); // X LESS THAN OR EQUAL TO y
        System.out.println("Hello World" instanceof Object); // is a String an instance of an Object

    }
}