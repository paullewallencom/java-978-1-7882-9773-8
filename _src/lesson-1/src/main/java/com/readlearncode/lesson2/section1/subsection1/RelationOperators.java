public class RelationOperators {

    public static void main(String... args) {

        short x = 10;
        byte y = 5;
        float z = 5.5f;

        System.out.println(x > y);
        System.out.println(z >= y);
        System.out.println(x < y);
        System.out.println(x <= y);

    }
}